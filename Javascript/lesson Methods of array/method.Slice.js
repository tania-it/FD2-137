let aa = ['A', 'B', 'C', 'D', 'E', 'F', 'G'];
console.log (aa.slice (3)); // выведет элементы массива, начиная с 3-го индекса и до конца массива.
console.log (aa.slice (3,5)); // выведет элементы массива, начиная 3-го элемента по 5-ый не влючая его.
console.log (aa.slice (3,-1)); // выведет элементы массива, начиная 3-го элемента по -1-ый с конца ('F'), включая его.
